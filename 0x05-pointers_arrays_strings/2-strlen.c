/**
 * _strlen - returns the length of a string
 * @s: strings to be checked
 *
 * Return: The total strings
 *
 * Author - AC Martins:
*/
int _strlen(char *s)
{
	int i, count;

	for (i = 0; s[i] != '\0'; i++)
	{
		count = i + 1;
	}
	return (count);
}
